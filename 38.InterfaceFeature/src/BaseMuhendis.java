public class BaseMuhendis {

    private String ad;

    private String soyad;

    private int tecrube;

    private double maas;

    private String[] diller;

    private String esgerlikVeziyyeti;

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSoyad() {
        return soyad;
    }

    public void setSoyad(String soyad) {
        this.soyad = soyad;
    }

    public int getTecrube() {
        return tecrube;
    }

    public void setTecrube(int tecrube) {
        this.tecrube = tecrube;
    }

    public double getMaas() {
        return maas;
    }

    public void setMaas(double maas) {
        this.maas = maas;
    }

    public String[] getDiller() {
        return diller;
    }

    public void setDiller(String[] diller) {
        this.diller = diller;
    }

    public String getEsgerlikVeziyyeti() {
        return esgerlikVeziyyeti;
    }

    public void setEsgerlikVeziyyeti(String esgerlikVeziyyeti) {
        this.esgerlikVeziyyeti = esgerlikVeziyyeti;
    }
}
